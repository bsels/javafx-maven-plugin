/// It is designed to facilitate analysis and manipulation of JavaFX controller components during processing.
///
/// Key parts of this package include:
/// - [io.github.bsels.javafx.maven.plugin.fxml.introspect.ControllerField]: Represents a field in a controller
/// - [io.github.bsels.javafx.maven.plugin.fxml.introspect.ControllerMethod]: Represents a method in a controller
/// - [io.github.bsels.javafx.maven.plugin.fxml.introspect.Visibility]: Defines access level visibility for controller
///   members
///
/// The classes in this package are primarily used for introspecting and analyzing JavaFX controller structures,
/// providing detailed information about their fields, methods, and visibility.
